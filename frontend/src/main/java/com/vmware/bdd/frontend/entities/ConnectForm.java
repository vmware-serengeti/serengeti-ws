package com.vmware.bdd.frontend.entities;

public class ConnectForm {

    private String serengetiServer;
    private String username;
    private String password;

    public String getSerengetiServer() {
        return serengetiServer;
    }

    public void setSerengetiServer(String serengetiServer) {
        this.serengetiServer = serengetiServer;
    }

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}
        
}
